package com.modelink.reservation.vo;

/**
 * 预约时间的VO
 */
public class ReserveDateVo {

    private String date;
    private String week;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getWeek() {
        return week;
    }

    public void setWeek(String week) {
        this.week = week;
    }
}
